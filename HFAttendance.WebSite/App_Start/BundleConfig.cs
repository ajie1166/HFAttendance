using System.Web;
using System.Web.Optimization;

namespace HFAttendance.WebSite
{
    public class BundleConfig
    {
        // For more information on bundling, visit https://go.microsoft.com/fwlink/?LinkId=301862
        public static void RegisterBundles(BundleCollection bundles)
        {
            /*
             * bundles.add(Bundle类型) 本质:是将Bundle类型添加到字典中 
               该字典是以new ScriptBundle(string virtualPath)中的参数virtualPath为key(键),以Bundle实例(new ScriptBundle(path).Include(静态文件的虚拟路径))为value(值)的字典*/
            bundles.Add(new ScriptBundle("~/js").Include(
                        "~/Scripts/jquery-{version}.js"));

            /*bundles.Add(new ScriptBundle("~/bundles/jqueryval").Include(
                        "~/Scripts/jquery.validate*"));*/

            // 使用要用于开发和学习的 Modernizr 的开发版本。然后，当你做好
            // ready for production, use the build tool at https://modernizr.com to pick only the tests you need.
            /* bundles.Add(new ScriptBundle("~/bundles/modernizr").Include(
                         "~/Scripts/modernizr-*"));*/

            bundles.Add(new ScriptBundle("~/bootstrap").Include(
                      "~/Scripts/bootstrap.js",
                      "~/Scripts/respond.js"));

            bundles.Add(new StyleBundle("~/css").Include(
                      "~/Content/bootstrap.min.css","~/Content/flat-ui.min.css","~/Content/demo.css"));
        }
    }
}
